// KST 기준 연중 일수(0부터). 서버 타임존과 무관하게 한국 달력 날짜로 계산.
function dayOfYearKST(now: Date): number {
  const kst = new Intl.DateTimeFormat('en-CA', {
    timeZone: 'Asia/Seoul', year: 'numeric', month: '2-digit', day: '2-digit',
  }).format(now); // YYYY-MM-DD
  const [y, m, d] = kst.split('-').map(Number);
  const start = Date.UTC(y, 0, 1);
  const cur = Date.UTC(y, m - 1, d);
  return Math.max(0, Math.floor((cur - start) / 86400000));
}

// 오늘(또는 전달 날짜)의 명언 — 같은 달력 날짜에는 항상 같은 문구.
// now 기본값은 호출(렌더) 시점의 현재 시각 — 모듈 로드 시점이 아니다.
export function getDailyQuote(now: Date = new Date()): string {
  if (QUOTES.length === 0) return '';
  const idx = dayOfYearKST(now) % QUOTES.length;
  return QUOTES[idx];
}
